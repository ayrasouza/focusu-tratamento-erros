from abc import ABfC, abstractmethod


# =====================================================
# SUPERCLASSE ABSTRATA
# =====================================================

class UsuarioBase(ABC):

    total_usuarios = 0

    def __init__(self, nome, email):

        self.nome = nome
        self.email = email

        UsuarioBase.total_usuarios += 1

    # =====================================
    # PROPERTY - NOME
    # =====================================

    @property
    def nome(self):
        return self._nome

    @nome.setter
    def nome(self, valor):

        if not valor.strip():
            raise ValueError(
                "Nome não pode ser vazio."
            )

        self._nome = valor

    # =====================================
    # PROPERTY - EMAIL
    # =====================================

    @property
    def email(self):
        return self._email

    @email.setter
    def email(self, valor):

        if "@" not in valor:
            raise ValueError(
                "Email inválido."
            )

        self._email = valor

    # =====================================
    # MÉTODO ABSTRATO
    # =====================================

    @abstractmethod
    def exibir_perfil(self):
        pass


# =====================================================
# CLASSE ALUNO
# =====================================================

class Aluno(UsuarioBase):

    def __init__(
        self,
        nome,
        email,
        matricula
    ):

        super().__init__(
            nome,
            email
        )

        self.matricula = matricula

        # Associação
        # Cada aluno possui disciplinas

        self.disciplinas = []

    def adicionar_disciplina(
        self,
        disciplina
    ):

        self.disciplinas.append(
            disciplina
        )

    def exibir_perfil(self):

        return (
            f"Aluno: {self.nome} | "
            f"Matrícula: {self.matricula}"
        )

    def __str__(self):

        return self.exibir_perfil()

# =====================================================
# INTERFACE
# =====================================================

class Publicavel(ABC):

    @abstractmethod
    def publicar(self):
        pass


# =====================================================
# CLASSE POSTAGEM
# =====================================================

class Postagem(Publicavel):

    total_postagens = 0

    def __init__(
        self,
        titulo,
        conteudo,
        autor
    ):

        self.titulo = titulo
        self.conteudo = conteudo
        self.autor = autor

        self.curtidas = 0
        self.comentarios = []

        Postagem.total_postagens += 1

    # =====================================
    # PROPERTY - TITULO
    # =====================================

    @property
    def titulo(self):
        return self._titulo

    @titulo.setter
    def titulo(self, valor):

        if not valor.strip():
            raise ValueError(
                "Título não pode ser vazio."
            )

        self._titulo = valor

    # =====================================
    # PROPERTY - CONTEUDO
    # =====================================

    @property
    def conteudo(self):
        return self._conteudo

    @conteudo.setter
    def conteudo(self, valor):

        if not valor.strip():
            raise ValueError(
                "Conteúdo não pode ser vazio."
            )

        self._conteudo = valor

    # =====================================
    # MÉTODOS DE INSTÂNCIA
    # =====================================

    def curtir(self):

        self.curtidas += 1

    def comentar(
        self,
        comentario
    ):

        self.comentarios.append(
            comentario
        )

    # =====================================
    # POLIMORFISMO
    # =====================================

    def publicar(self):

        return (
            f"\nPOSTAGEM\n"
            f"Título: {self.titulo}\n"
            f"Autor: {self.autor.nome}\n"
            f"Conteúdo: {self.conteudo}\n"
            f"Curtidas: {self.curtidas}\n"
            f"Comentários: {len(self.comentarios)}"
        )

    def __str__(self):

        return self.publicar()


