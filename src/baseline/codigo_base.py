# =====================================================

class Rotina:

    total_rotinas = 0

    def __init__(
        self,
        atividade,
        tempo
    ):

        self.atividade = atividade
        self.tempo = tempo

        Rotina.total_rotinas += 1

    @property
    def tempo(self):
        return self._tempo

    @tempo.setter
    def tempo(self, valor):

        if valor <= 0:
            raise ValueError(
                "Tempo deve ser maior que zero."
            )

        self._tempo = valor

    def __str__(self):

        return (
            f"Atividade: {self.atividade} | "
            f"Tempo: {self.tempo} min"
        )


