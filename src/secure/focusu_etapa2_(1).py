from abc import ABC, abstractmethod

# Classe que serve de modelo de herança. Possui atributos
# estáticos de contagem e métodos abstratos herdados.

class UsuarioBase(ABC):
    total_usuarios = 0

    def __init__(self, nome, email):
        self._nome = nome
        self._email = email
        UsuarioBase.total_usuarios += 1

    def __del__(self):
        if UsuarioBase.total_usuarios > 0:
            UsuarioBase.total_usuarios -= 1


    # CONCEITO: ENCAPSULAMENTO
    @property
    def nome(self):
        return self._nome

    @nome.setter
    def nome(self, valor):
        if not valor.strip():
            raise ValueError("Nome não pode ser vazio.")
        self._nome = valor

    @property
    def email(self):
        return self._email

    @email.setter
    def email(self, valor):
        if "@" not in valor:
            raise ValueError("Email inválido.")
        self._email = valor


    # CONCEITO: MÉTODO ABSTRATO
    # Força as classes filhas a implementarem sua própria versão.

    @abstractmethod
    def exibir_perfil(self):
        pass


# CONCEITO: HERANÇA
# Classe Aluno estende a classe abstrata UsuarioBase.

class Aluno(UsuarioBase):
    def __init__(self, nome, email, matricula):
        super().__init__(nome, email)
        self.matricula = matricula


        # CONCEITO: ASSOCIAÇÃO
        # Armazena internamente referências de objetos de outras classes.
        self.disciplinas = []
        self.rotinas = []

    def adicionar_disciplina(self, disciplina):
        self.disciplinas.append(disciplina)

    def adicionar_rotina(self, rotina):
        self.rotinas.append(rotina)

    def exibir_perfil(self):
        return f"Aluno: {self.nome} | Matrícula: {self.matricula}"

    def __str__(self):
        return self.exibir_perfil()


# CONCEITO: INTERFACE
# Define um contrato de comportamento obrigatório para o Feed.

class Publicavel(ABC):
    @abstractmethod
    def publicar(self):
        pass


# CONCEITO: IMPLEMENTAÇÃO DE INTERFACE
# Classe Postagem assina e implementa o contrato de Publicavel.

class Postagem(Publicavel):
    total_postagens = 0

    def __init__(self, titulo, conteudo, autor):
        self.titulo = titulo
        self.conteudo = conteudo
        self.autor = autor
        self.curtidas = 0
        self.comentarios = []
        Postagem.total_postagens += 1

    def __del__(self):
        if Postagem.total_postagens > 0:
            Postagem.total_postagens -= 1

    @property
    def titulo(self):
        return self._titulo

    @titulo.setter
    def titulo(self, valor):
        if not valor.strip():
            raise ValueError("Título não pode ser vazio.")
        self._titulo = valor

    @property
    def conteudo(self):
        return self._conteudo

    @conteudo.setter
    def conteudo(self, valor):
        if not valor.strip():
            raise ValueError("Conteúdo não pode ser vazio.")
        self._conteudo = valor

    def curtir(self):
        self.curtidas += 1

    def comentar(self, comentario):
        self.comentarios.append(comentario)

    def publicar(self):
        nome_autor = self.autor.nome if self.autor else "Usuário Anônimo"
        return (
            f"\n[POST GERAL]\n"
            f"Título: {self.titulo}\n"
            f"Autor: {nome_autor}\n"
            f"Conteúdo: {self.conteudo}\n"
            f"Curtidas: {self.curtidas} | Comentários: {len(self.comentarios)}"
        )


# CONCEITO: HERANÇA E POLIMORFISMO
# Especializações de Postagem que alteram o método publicar().

class PostagemDuvida(Postagem):
    def __init__(self, titulo, conteudo, autor, disciplina):
        super().__init__(titulo, conteudo, autor)
        self.disciplina = disciplina
        self.resolvida = False

    # Sobrescrita Polimórfica: adiciona status e metadados específicos
    def publicar(self):
        nome_autor = self.autor.nome if self.autor else "Usuário Anônimo"
        status = "[RESOLVIDA]" if self.resolvida else "[ABERTA]"
        return (
            f"\n[DÚVIDA - {self.disciplina.upper()}] {status}\n"
            f"Título: {self.titulo}\n"
            f"Autor: {nome_autor}\n"
            f"Conteúdo: {self.conteudo}\n"
            f"Curtidas: {self.curtidas} | Comentários: {len(self.comentarios)}"
        )


class PostagemMaterial(Postagem):
    def __init__(self, titulo, conteudo, autor, link_download):
        super().__init__(titulo, conteudo, autor)
        self.link_download = link_download

    # Sobrescrita Polimórfica: inclui link de download na visualização
    def publicar(self):
        nome_autor = self.autor.nome if self.autor else "Usuário Anônimo"
        return (
            f"\n[MATERIAL COMPARTILHADO]\n"
            f"Título: {self.titulo}\n"
            f"Autor: {nome_autor}\n"
            f"Conteúdo: {self.conteudo}\n"
            f"Link de Download: {self.link_download}\n"
            f"Curtidas: {self.curtidas} | Comentários: {len(self.comentarios)}"
        )


